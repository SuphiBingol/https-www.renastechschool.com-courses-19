package day8_String;

public class c2_StringPractice {


    public static void main(String[] args) {

      int w = 30;

      System.out.println(w--);//30
      System.out.println(w--);//29
      System.out.println(--w);//27
      System.out.println(w);//27
      System.out.println(w++);//27
      System.out.println(w);//28;



        // &&
        //|| --or
        // ! -- not

        System.out.println((8<9) || (5<6));
        System.out.println((4>8) || (8>7));
        System.out.println((5<3) || (9<7));

        System.out.println((5==6)|| (5!=6));
        System.out.println();





    }






}
